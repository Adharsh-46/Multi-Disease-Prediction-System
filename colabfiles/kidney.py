data.dropna(inplace=True)

data

# Convert 'classification' to numerical
data['classification'] = data['classification'].replace({'ckd': 0, 'notckd': 1})

# Drop 'id' column as it's not a feature
if 'id' in data.columns:
    data = data.drop('id', axis=1)

# Convert 'pcv', 'wc', 'rc' to numeric, coercing errors
data['pcv'] = pd.to_numeric(data['pcv'], errors='coerce')
data['wc'] = pd.to_numeric(data['wc'], errors='coerce')
data['rc'] = pd.to_numeric(data['rc'], errors='coerce')

# Re-apply dropna after numerical conversion, in case new NaNs were introduced
# from coercing non-numeric values in 'pcv', 'wc', 'rc'.
data.dropna(inplace=True)

# Define a mapping dictionary for common binary categories
binary_mapping = {
    'rbc': {'normal': 0, 'abnormal': 1},
    'pc': {'normal': 0, 'abnormal': 1},
    'pcc': {'notpresent': 0, 'present': 1},
    'ba': {'notpresent': 0, 'present': 1},
    'htn': {'no': 0, 'yes': 1},
    'dm': {'no': 0, 'yes': 1},
    'cad': {'no': 0, 'yes': 1},
    'appet': {'poor': 0, 'good': 1},
    'pe': {'no': 0, 'yes': 1},
    'ane': {'no': 0, 'yes': 1}
}

# Apply the mapping to categorical columns
for col, mapping in binary_mapping.items():
    if col in data.columns:
        data[col] = data[col].map(mapping)

# Define features (x) and target (y)
x = data.drop('classification', axis=1)
y = data['classification']

x_train, x_test, y_train, y_test = train_test_split(x, y, test_size=0.2, random_state=42,stratify=y)

models = {
    "Logistic Regression": LogisticRegression(max_iter=1000),
    "Random Forest": RandomForestClassifier(n_estimators=100, random_state=42),
    "XGBoost": XGBClassifier(use_label_encoder=False, eval_metric='logloss', random_state=42)
}

best_model = None
best_score = 0
best_name = ""

for name, model in models.items():
    model.fit(x_train, y_train)
    y_pred_test = model.predict(x_test)

    # ===== Performance Matrix =====
    report = classification_report(y_test, y_pred_test)
    print(f"\nREPORT OF THE {name} Model:\n", report)

    score = accuracy_score(y_test, y_pred_test)
    print(f"Score = {score}")

    # Save the best model
    if score > best_score:
        best_score = score
        best_model = model
        best_name = name

# Save the Best Model
if best_model:
    import joblib


