# ==========================================
# Import Libraries
# ==========================================

import numpy as np
import pandas as pd
import pickle

from sklearn.cluster import KMeans
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import accuracy_score, confusion_matrix, classification_report
from sklearn.model_selection import train_test_split


# ==========================================
# Load Dataset
# ==========================================

data = pd.read_csv('/content/data.csv')

print("First 5 rows")
print(data.head())

print("\nDataset Info")
print(data.info())

print("\nDataset Shape:", data.shape)


# ==========================================
# Data Preprocessing
# ==========================================

# Convert diagnosis to numeric
# M = Malignant (1)
# B = Benign (0)

data['diagnosis'] = data['diagnosis'].map({'M':1, 'B':0})

# Separate features and labels
X = data.drop(columns=['diagnosis','id'], errors='ignore')
y = data['diagnosis']


# ==========================================
# Train Test Split
# ==========================================

X_train, X_test, y_train, y_test = train_test_split(
    X,
    y,
    test_size=0.25,
    random_state=42
)


# ==========================================
# Feature Scaling
# ==========================================

scaler = StandardScaler()

X_train = scaler.fit_transform(X_train)
X_test = scaler.transform(X_test)


# ==========================================
# K-Means Clustering Model
# ==========================================

kmeans = KMeans(
    n_clusters=2,
    random_state=42
)

kmeans.fit(X_train)


# ==========================================
# Prediction
# ==========================================

clusters = kmeans.predict(X_test)


# ==========================================
# Accuracy Adjustment
# (Clusters may be reversed)
# ==========================================

accuracy1 = accuracy_score(y_test, clusters)
accuracy2 = accuracy_score(y_test, 1 - clusters)

accuracy = max(accuracy1, accuracy2)

print("\nModel Accuracy:", accuracy)


# ==========================================
# Confusion Matrix
# ==========================================

print("\nConfusion Matrix")
print(confusion_matrix(y_test, clusters))


# ==========================================
# Classification Report
# ==========================================

print("\nClassification Report")
print(classification_report(y_test, clusters))


